package dev.xlin.gameworkshop.progs.grmps.beans;

/**
 *
 * @author 刘祎鹏
 */
public class beanGrmpMapNode
{
    private int OID = 0 ;
    private String nodeName = "";
    private String nodeTag = "";
    private int mapID = 0 ;
    private int defaultVisb = 0;
    private double x = 0 ;
    private double y = 0 ;
    private double z = 0 ;

    public int getOID()
    {
        return OID;
    }

    public void setOID(int OID)
    {
        this.OID = OID;
    }

    public String getNodeName()
    {
        return nodeName;
    }

    public void setNodeName(String nodeName)
    {
        this.nodeName = nodeName;
    }

    public String getNodeTag()
    {
        return nodeTag;
    }

    public void setNodeTag(String nodeTag)
    {
        this.nodeTag = nodeTag;
    }

    public int getMapID()
    {
        return mapID;
    }

    public void setMapID(int mapID)
    {
        this.mapID = mapID;
    }

    public int getDefaultVisb()
    {
        return defaultVisb;
    }

    public void setDefaultVisb(int defaultVisb)
    {
        this.defaultVisb = defaultVisb;
    }

    public double getX()
    {
        return x;
    }

    public void setX(double x)
    {
        this.x = x;
    }

    public double getY()
    {
        return y;
    }

    public void setY(double y)
    {
        this.y = y;
    }

    public double getZ()
    {
        return z;
    }

    public void setZ(double z)
    {
        this.z = z;
    }
    
}

