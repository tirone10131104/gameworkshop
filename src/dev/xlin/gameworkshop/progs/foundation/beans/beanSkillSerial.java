public class beanSkillSerial
{
    private int OID = 0 ;
    private String serialName = "";
    private String serialTag = "";
    private String serialSkills = "";

    public int getOID()
    {
        return OID;
    }

    public void setOID(int OID)
    {
        this.OID = OID;
    }

    public String getSerialName()
    {
        return serialName;
    }

    public void setSerialName(String serialName)
    {
        this.serialName = serialName;
    }

    public String getSerialTag()
    {
        return serialTag;
    }

    public void setSerialTag(String serialTag)
    {
        this.serialTag = serialTag;
    }

    public String getSerialSkills()
    {
        return serialSkills;
    }

    public void setSerialSkills(String serialSkills)
    {
        this.serialSkills = serialSkills;
    }
    
}

