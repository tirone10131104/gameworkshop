package dev.xlin.gameworkshop.progs.contents.beans;

/**
 *
 * @author maomao
 */
public class poObjectPhysic
{

    //质量
    private double mass = 0.0;
    //密度
    private double density = 0.0;
    //表面温度
    private double tempreture = 0.0;
    //直径
    private double diameter = 0.0;
    //光度
    private double luminosity = 0.0;
    //表面引力
    private double gravity = 0.0;
    //逃逸速度
    private double cosmic1 = 0.0;
    //第二宇宙速度
    private double cosmic2 = 0.0;
    //绝对星等
    private double absMang = 0.0;

    public double getMass()
    {
        return mass;
    }

    public void setMass(double mass)
    {
        this.mass = mass;
    }

    public double getDensity()
    {
        return density;
    }

    public void setDensity(double density)
    {
        this.density = density;
    }

    public double getTempreture()
    {
        return tempreture;
    }

    public void setTempreture(double tempreture)
    {
        this.tempreture = tempreture;
    }

    public double getDiameter()
    {
        return diameter;
    }

    public void setDiameter(double diameter)
    {
        this.diameter = diameter;
    }

    public double getLuminosity()
    {
        return luminosity;
    }

    public void setLuminosity(double luminosity)
    {
        this.luminosity = luminosity;
    }

    public double getGravity()
    {
        return gravity;
    }

    public void setGravity(double gravity)
    {
        this.gravity = gravity;
    }

    public double getCosmic1()
    {
        return cosmic1;
    }

    public void setCosmic1(double cosmic1)
    {
        this.cosmic1 = cosmic1;
    }

    public double getCosmic2()
    {
        return cosmic2;
    }

    public void setCosmic2(double cosmic2)
    {
        this.cosmic2 = cosmic2;
    }

    public double getAbsMang()
    {
        return absMang;
    }

    public void setAbsMang(double absMang)
    {
        this.absMang = absMang;
    }

}
