public class BeanCtxConstellation
{
    
    private int OID = 0;
    //星系ID
    private int glxOID = 0;
    //星域ID
    private int regionOID = 0;
    //星座名称
    private String conName = "";
    //星座标签
    private String conTag = "";
    //描述
    private String conDesp = "";
    //配置字符串
    private String configs = "";

    public int getOID()
    {
        return OID;
    }

    public void setOID(int OID)
    {
        this.OID = OID;
    }

    public int getGlxOID()
    {
        return glxOID;
    }

    public void setGlxOID(int glxOID)
    {
        this.glxOID = glxOID;
    }

    public int getRegionOID()
    {
        return regionOID;
    }

    public void setRegionOID(int regionOID)
    {
        this.regionOID = regionOID;
    }

    public String getConName()
    {
        return conName;
    }

    public void setConName(String conName)
    {
        this.conName = conName;
    }

    public String getConTag()
    {
        return conTag;
    }

    public void setConTag(String conTag)
    {
        this.conTag = conTag;
    }

    public String getConDesp()
    {
        return conDesp;
    }

    public void setConDesp(String conDesp)
    {
        this.conDesp = conDesp;
    }

    public String getConfigs()
    {
        return configs;
    }

    public void setConfigs(String configs)
    {
        this.configs = configs;
    }

}

